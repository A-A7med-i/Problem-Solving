# include <iostream>
using namespace std;

int main(){
    int long long X,Y;
    cin>>X>>Y;
    int sum = X + Y;
    int long long multiplication = X * Y;
    int subtraction = X - Y;
    cout<<X<<" + "<<Y<<" = "<<sum<<endl;
    cout<<X<<" * "<<Y<<" = "<<multiplication<<"\n";
    cout<<X<<" - "<<Y<<" = "<<subtraction<<endl;
}



